#!/usr/bin/env python3
"""
Module that provides string conversion operations with type annotations.

This module contains functions for converting data types to strings
using Python 3 type annotations for better code clarity and type checking.
"""


def to_str(n: float) -> str:
    """
    Convert a floating point number to its string representation.

    Args:
        n (float): The floating point number to convert.

    Returns:
        str: The string representation of n.
    """
    return str(n)
